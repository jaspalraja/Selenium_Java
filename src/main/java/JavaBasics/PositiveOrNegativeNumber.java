package JavaBasics;

public class PositiveOrNegativeNumber {

	public static void main(String[] args) {
		// if else ladder
		int num = -9;
		if (num > 0) {
			System.out.println("Number is Positive");
		} else if (num < 0) {
			System.out.println("Number is Negative");
		} else {
			System.out.println("number is zero");
		}

	}

}
